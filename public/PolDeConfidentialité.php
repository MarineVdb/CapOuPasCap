En conservant des informations sur vos visites précédentes, ils vous 
aident également à accéder au Site de façon plus rapide et efficace.</p>
<p style="text-align:justify">9-2 Les cookies générées par des sociétés tierces</p>
<p style="text-align:justify">Nous pouvons être amenés à inclure sur notre site des applications 
informatiques émanant de tiers afin de vous permettre de partager des 
contenus de notre site ou votre opinion sur nos produits avec d’autres 
personnes, via des réseaux sociaux (ex&nbsp;: Facebook, 
Instagram…) ou via des sites internet (ex&nbsp;: 
&nbsp;Youtube…) en cliquant notamment sur des boutons 
«&nbsp;partager&nbsp;» «&nbsp;like&nbsp;» ou en mentionnant des 
commentaires.</p>
<p style="text-align:justify">Ces réseaux sociaux et sites internet sont susceptibles de vous 
identifier lors de votre navigation sur leurs applications inclues sur 
notre site. Cap ou pas Cap n’a pas d’information et de maitrise sur 
l’utilisation et le traitement de vos données personnelles par ces 
sites. Il convient de vous reporter à leurs propres politiques de 
protection des données personnelles.</p>
<p style="text-align:justify">9-3 La gestion et la maitrise des cookies</p>
<p style="text-align:justify">La plupart des navigateurs internet acceptent l’utilisation de 
cookies par défaut et en accédant au Site, vous consentez à cette 
utilisation.</p>
<p style="text-align:justify">Vous pouvez néanmoins changer vos paramètres de navigation pour 
contrôler l’usage des cookies ou les interdire. Si vous choisissez cette
 option, vous devez savoir que le Site pourrait ne pas fonctionner 
correctement, voire ne pas fonctionner du tout. Les modalités de 
changement de ces paramètres varient selon chaque navigateur (tels que 
Internet Explorer, Firefox, Safari, Chrome etc.), et nous vous invitons à
 consulter le menu « Aide » de votre navigateur pour toute information 
complémentaire.</p>
<p style="text-align:justify">9-4 Les cookies&nbsp;</p>
<p style="text-align:justify">Les cookies gérés par Cap ou pas Cap sont conservés au maximum pour une durée de 13 mois.&nbsp;</p>
