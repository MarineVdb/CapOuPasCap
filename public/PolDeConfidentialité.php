 ans pour communiquer leurs données personnelles. Cap ou pas Cap ne 
recueille donc pas de données d’enfants de moins de 16 ans et, s’il 
s’avérait que, par erreur, de telles données étaient recueillies, il 
conviendrait d’en informer immédiatement Cap ou pas Cap afin de permettre leur suppression.</p>
<p style="text-align:justify">2-2 Recueil direct&nbsp;</p>
<p style="text-align:justify">Il s’agit des informations collectées en ligne sur notre site 
