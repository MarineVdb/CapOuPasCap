<p style="text-align:justify">4 - LES DESTINATAIRES DE CES DONNEES PERSONNELLES&nbsp;:&nbsp;</p>
<p style="text-align:justify">4-1 Au sein de la boutique Cap ou pas Cap</p>
<p style="text-align:justify">Vos données personnelles ne seront accessibles qu’aux salariés qui 
auront besoin d’avoir connaissance de ces données pour accomplir leurs 
missions. Ces salariés devront être autorisés et disposer d’une 
connexion sécurisée au traitement.</p>
<p style="text-align:justify">Ainsi, si vous êtes un client actuel ou un prospect de Cap ou pas Cap,
 seuls les salariés de Cap ou pas Cap qui travaillent au sein des équipes
 ayant besoin de détenir vos informations et qui traitent ces 
informations (Les salariés des équipes Digital et Retail en charge de la
 Relation Client) auront accès à vos données personnelles.</p>
<p style="text-align:justify">Si vous avez adressé une candidature, seuls les salariés du service 
des ressources humaines en charge du recrutement auront accès à vos 
données.&nbsp;</p>
<p style="text-align:justify">4-2 En dehors de la boutique Cap ou pas Cap</p>
<p style="text-align:justify">Vos données personnelles ne seront pas communiquées à des tiers, 
excepté à d'autres entités Cap ou pas Cap &nbsp;situées en France, pour 
les fins susmentionnées, et à nos prestataires de services de confiance 
(ex&nbsp;: prestataires de services de maintenance, de sécurité et 
d'assistance informatiques, fournisseurs d'hébergement, fournisseurs de 
services de paiement, etc…) agissant pour le compte de Cap ou pas Cap, 
sur la base d'accords contractuels sécurisés qui incluent des 
obligations strictes en matière de protection des données.</p>
<p style="text-align:justify">Cap ou pas Cap peut également être contrainte de transférer des 
données personnelles à des tiers si des autorités habilitées le lui 
demandent, conformément aux lois et réglementations en vigueur.</p>
<p style="text-align:justify">En cas de fusion ou de vente, vos données personnelles seront transférées de façon définitive à notre successeur.<br> &nbsp;</p>
<p style="text-align:justify">5 - LES TRANSFERTS DE DONNEES A L’ETRANGER</p>
<p style="text-align:justify">Cap ou pas Cap ne transfère aucune de vos Données Personnelles à l'étranger.</p>
<p style="text-align:justify">Dans le cas où un tel transfert serait mis en place, notamment en 
dehors de l'Union Européenne, celui-ci serait effectué moyennant des 
garanties appropriées et en conformité avec la règlementation applicable
 en matière de protection des données personnelles.<br> &nbsp;</p>
<p style="text-align:justify">6 - LA CONSERVATION DES DONNEES PERSONNELLES&nbsp;</p>
<p style="text-align:justify">Cap ou pas Cap a besoin, pour vous garantir des services de qualité et
 personnalisés, de conserver vos données personnelles le temps 
nécessaire pour pouvoir fournir lesdits services et pour remplir ses 
obligations légales.&nbsp;Cap ou pas Cap conserve vos 
données&nbsp;:&nbsp;</p>
<p style="text-align:justify">6-1 Si vous êtes Client Cap ou pas Cap</p>
<p style="text-align:justify">En raison de la qualité et de la nature des produits et services Cap ou pas Cap, les durées de vie, de cycle d’achats et de service 
après-vente sont plus importants que pour des produits de consommation 
ordinaire et nécessitent un suivi Client adapté ainsi que des délais 
adaptés.</p>
<p style="text-align:justify">Les données de votre compte Client&nbsp;sont ainsi conservées et 
utilisées pour une durée maximale de 3 années à compter de votre dernier
 achat ou interaction avec la marque Cap ou pas Cap (sollicitation du 
service client, participation à un évènement). Au terme de ce délai de 3
 ans, Cap ou pas Cap pourra reprendre contact avec vous afin de savoir si
 vous souhaitez ou non continuer à recevoir les informations sur ses 
offres, actualités et évènements.&nbsp;</p>
<p style="text-align:justify">Les données relatives aux transactions passées avec Cap ou pas Cap 
sont conservées et archivées pour des durées pouvant varier selon le 
type de données et n’excédant pas 10 ans afin que Cap ou pas Cap puisse 
répondre à ses obligations administratives et légale. Au-delà de ces 
délais les données seront détruites.&nbsp;</p>
<p style="text-align:justify">6-2 Si vous êtes un Prospect Cap ou pas Cap</p>
<p style="text-align:justify">C’est-à-dire si vous avez sollicité des informations sur Cap ou pas Cap mais que vous n’avez jamais acheté de produits Cap ou pas Cap ou 
crée de compte Client Cap ou pas Cap.&nbsp;</p>
<p style="text-align:justify">Vos données, que vous avez accepté de nous transmettre lors de votre 
inscription à notre Newsletter, sont conservées pour une durée de 3 ans à
 compter de la collecte de vos données. Cette période est renouvelée à 
chaque fois que vous interagissez avec Cap ou pas Cap (par exemple une 
participation à un évènement). Au terme de ce délai de 3 ans, Cap ou pas Cap pourra reprendre contact avec vous afin de savoir si vous souhaitez
 ou non continuer à recevoir les informations sur ses offres, actualités
 et évènements.</p>
<p style="text-align:justify">6-3 Si vous êtes candidat pour des opportunités professionnelles au sein de chez Cap ou pas Cap</p>
<p style="text-align:justify">Vos données seront conservées 2 années à compter de votre candidature.</p>
<p style="text-align:justify">6-4 Les données de carte bancaire&nbsp;</p>
<p style="text-align:justify">Conservation des données de carte bancaire à la suite d’un 
paiement&nbsp;: ces données sont conservées 13 mois par un prestataire 
de paiement sécurisé.</p>
<p style="text-align:justify">Conservation de la carte bancaire dans le portefeuille électronique 
pour les achats ultérieurs&nbsp;: ces données sont conservées par un 
prestataire de paiement sécurisé uniquement si vous y avez consenti et 
jusqu’au retrait de votre consentement ou à l’expiration de votre carte 
bancaire.<br> &nbsp;</p>
<p style="text-align:justify">7 -&nbsp;SECURITE&nbsp;</p>
<p style="text-align:justify">Cap ou pas Cap a mis en place les mesures techniques et physiques 
appropriées pour assurer la confidentialité et la sécurité de vos 
données personnelles.</p>
<p style="text-align:justify">Les systèmes informatiques de Cap ou pas Cap font l’objet d’une 
protection physique et informatique conforme à l’état de l’art. Des 
procédures de contrôle d’accès aux locaux, aux ordinateurs, aux serveurs
 et aux logiciels sont mis en place. En outre la sauvegarde physique et 
électronique des données collectées sont mises en œuvre, conformément à 
la législation française en vigueur relatives à la protection des 
données personnelles et aux standards du marché.</p>
<p style="text-align:justify">Les salariés Cap ou pas Cap qui, du fait de leur fonction, auraient 
accès à vos Données Personnelles sont soumis à des obligations de 
confidentialité strictes.</p>
<p style="text-align:justify">Veuillez être conscient, toutefois, qu'aucune transmission de données
 sur Internet n'est sécurisée à 100 % et que toutes les informations 
communiquées en ligne peuvent être potentiellement interceptées et 
utilisées par d'autres personnes que le destinataire souhaité.<br> &nbsp;</p>
<p style="text-align:justify">8 - LES DROITS DES CLIENTS ET POSPECTS&nbsp;</p>
<p style="text-align:justify">Vous disposez de différents droits afin de vous permettre de mieux 
protéger et maitriser la protection et l’usage de vos données 
personnelles&nbsp;:</p>
<ul>
<li>Un droit d'information :&nbsp;vous avez le droit d'être informé(e) de la façon dont vos données personnelles sont traitées.</li>
<li>Un droit d'accès et de rectification&nbsp;: vous avez le droit 
d'obtenir la confirmation que des données personnelles vous concernant 
sont traitées ou non, et, le cas échéant, d'accéder auxdites données 
personnelles et d'en obtenir une copie. Vous disposez d’un droit de 
rectification de vos données personnelles afin de les corriger, 
actualiser ou compléter.</li>
<li>Un droit à l'effacement :&nbsp;dans certains cas, vous avez le droit
 d'obtenir l’effacement de vos données personnelles. Toutefois, il ne 
s'agit pas d'un droit absolu et Cap ou pas Cap peut avoir des raisons 
légales ou légitimes de conserver ces données.</li>
<li>Un droit à la limitation du traitement :&nbsp;dans certains cas, 
vous avez le droit d'obtenir une limitation du traitement de vos données
 personnelles.</li>
<li>Un droit à la portabilité des données :&nbsp;vous avez le droit de 
recevoir les données personnelles que vous avez fournies à Cap ou pas Cap
 sous une forme structurée, couramment utilisée et lisible par une 
machine, et vous pouvez les transmettre à un autre responsable de 
traitement sans que Cap ou pas Cap y fasse obstacle. Ce droit ne 
s'applique que si le traitement de vos données personnelles repose sur 
votre consentement ou sur l’exécution d’un contrat et que ce traitement 
s'effectue à l’aide de procédés automatisés.</li>
<li>Un droit d'opposition au traitement :&nbsp;vous avez le droit de 
vous opposer à tout moment au traitement de vos données personnelles à 
des fins de prospection commerciale.</li>
<li>Un droit de déposer plainte auprès des autorités de contrôle 
compétentes en cas de violation des lois et réglementations relatives à 
la protection des données en vigueur, notamment le RGPD, mais aussi 
toute autre loi nationale ou régionale relative à la protection des 
données qui vous concerne.</li>
</ul>
<p style="text-align:justify">Pour l’exercice de ces droits, vous devez contacter Cap ou pas Cap en 
contactant le Délégué à la protection des données Cap ou pas Cap 
 et en communiquant les 
informations suivantes : prénom, nom, adresse e-mail ou toute autre 
information nécessaire pour confirmer votre identité et nous permettre 
de traiter votre demande, telle que votre adresse postale par exemple. 
Pour assurer la plus grande sécurité de vos données, un justificatif de 
votre identité pourra être demandé avant que votre demande ne soit 
traitée.</p>
<p style="text-align:justify">Vous pouvez également décider de vous désinscrire de notre 
Newsletter, de nos courriers ou de nos SMS à tout moment en respectant 
la procédure suivante&nbsp;: en vous désinscrivant à tout moment de la 
Newsletter sur votre compte client, en cliquant sur le lien de 
désabonnement joint à chaque courriel ou en contactant le service client
et demander à ne plus recevoir la newsletter, les courriers ou les SMS.<br> &nbsp;</p>
<p style="text-align:justify">9 - LES COOKIES&nbsp;</p>
<p style="text-align:justify">Les cookies sont des fichiers qui stockent de petites quantités de 
données liées à votre usage du Site (incluant votre adresse IP) sur le 
disque dur de votre ordinateur. Les données collectées par les cookies 
utilisés sur le Site sont anonymes et n’incluent pas de données 
personnelles.</p>
<p style="text-align:justify">9-1 Les cookies générés par Cap ou pas Cap</p>
<p style="text-align:justify">Les cookies générés par notre serveur nous permettent pour certains 
de faire fonctionner notre site internet et notamment les commandes, 
pour d’autres de surveiller le trafic sur le Site, d’identifier les 
centres d’intérêt des visiteurs, de procéder à des analyses et d’adapter
