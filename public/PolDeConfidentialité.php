<p style="text-align:justify">2 – LES DONNEES PERSONNELLES COLLECTEES ET LEUR RECUEIL&nbsp;:</p>
<p style="text-align:justify">Une donnée personnelle est définie comme toute information relative à
 une personne physique identifiée ou qui peut être identifiée, 
directement ou indirectement, par référence à un numéro d’identification
 ou à un ou plusieurs éléments qui lui sont propres tels que son nom, 
son prénom, sa date de naissance, son numéro client ou de commande, sa 
photo, etc…</p>
